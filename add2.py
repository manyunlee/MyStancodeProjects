import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> None:
    #######################
    # listnode convert to int: 2->4->3 convert to 342
    num1 = create_num(l1)
    num2 = create_num(l2)
    total=str(num1+num2)
    head=None
    #Convert to listnode
    for ch in str(total):
        new_node=ListNode(ch,None)
        if head is None:
            #First data
            head=new_node
            # cur=head
        else: #prepend
            new_node.next=head
            head=new_node

    return head


def create_num(listnode):
    cur = listnode
    lst = []  # save as [2,4,3]
    counter = 0
    num = 0
    while cur is not None:
        lst.append(cur.val)
        cur = cur.next
    # num:3*10^2+4*10^1+2*10^0
    for i in range(len(lst)):
        num += lst[i] * (10 ** (counter))  # counter=0: lst[0]=2, 2**10**0
        counter += 1
    return num


